/**
 * 為不同頁面生成對應的關鍵字組合
 */
export function getPageKeywords(page: 'home' | 'services' | 'photography'): string[] {
  const baseKeywords = [
    ...seoKeywords.personal.names,
    ...seoKeywords.personal.identity.slice(0, 3),
  ];

  switch (page) {
    case 'home':
      return [
        ...baseKeywords,
        ...seoKeywords.software.roles.slice(0, 5),
        ...seoKeywords.education.roles.slice(0, 3),
        ...seoKeywords.photography.roles.slice(0, 3),
        ...seoKeywords.achievements.awards.slice(0, 2),
        ...seoKeywords.location.slice(0, 3),
      ];
    
    case 'services':
      return [
        ...baseKeywords,
        ...seoKeywords.software.services,
        ...seoKeywords.education.roles,
        ...seoKeywords.software.skills.slice(0, 10),
        ...seoKeywords.entrepreneurship.slice(0, 5),
      ];
    
    case 'photography':
      return [
        ...baseKeywords,
        ...seoKeywords.photography.roles,
        ...seoKeywords.photography.projects,
        ...seoKeywords.photography.style,
      ];
    
    default:
      return baseKeywords;
  }
}

/**
 * 生成結構化的關鍵字字串
 */
export function generateKeywordsString(keywords: string[]): string {
  return keywords.join(', ');
}

